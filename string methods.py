name = "harsh"

# length of string
print(len(name))

# find a charactor
print(name.find("h"))

# capitalize the string (sentence case)
print(name.capitalize())

# convert string to uppercase
print(name.upper())

# convert string to lowercase
print(name.lower())

# check if the string contains only digit
print(name.isdigit())

# to check if the string contains only alphabets
print(name.isalpha())

# to count the number of a charactor in the string
print(name.count("h"))

# to replace the charactor from string
print(name.replace("h", "H"))

# to display a string multiple times
print(name*3)

