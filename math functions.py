import math
pi = 3.14

# round the integer to the nearest whole number
print(round(pi))

# round the number up 3.14 to 4
print(math.ceil(pi))

# to round the number down 3.14 to 3
print(math.floor(pi))

# gives us absolute value means how far a number is away from 0
print(abs(pi))

# pi^^2 adds power to the number
print(pow(pi,2))

# to find the square root
print(math.sqrt(144))

# to find the largest value
print(max(1,2,3))

# to find the lowest value
print(min(1,2,3))